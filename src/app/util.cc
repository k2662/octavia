#include "app/util.hh"

void dbg_log(std::string const& head, std::string const& body) {
  namespace aec = OB::Term::ANSI_Escape_Codes;
  std::cerr << aec::fg_256(std::to_string(std::hash<std::string>{}(head) % 255)) << head << aec::fg_white << "> " << aec::clear << body << "\n";
}
